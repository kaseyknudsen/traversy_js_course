let x;

x = Math.sqrt(9);
console.log(x)

//gives us the absolute number (removes any negative sign)
x = Math.abs(9);
console.log(x)

x = Math.round(9.9);
console.log(x)

x = Math.ceil(4.2);
console.log(x)

x = Math.floor(9.4);
console.log(x)

//to the power of
x = Math.pow(2, 3);
console.log(x)


x = Math.min(4, 5);
console.log(x)

x = Math.max(4, 5);
console.log(x)

x = Math.random()
console.log(x)

